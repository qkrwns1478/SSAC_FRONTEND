export const QUIZ_PER_ROUND = 3;

/** 오늘 날짜 기반 시드로 전체 풀을 셔플 — 라운드별 슬라이싱은 QuizWidget이 담당 */
export function getDailyQuiz(): QuizItem[] {
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
  return [...QUIZ_BANK].sort((a, b) => ((a.id * seed) % 97) - ((b.id * seed) % 97));
}

/** pool에서 seenIds를 제외한 다음 라운드 문제를 반환 */
export function getNextRound(pool: QuizItem[], seenIds: number[]): QuizItem[] {
  const available = pool.filter((q) => !seenIds.includes(q.id));
  const source = available.length >= QUIZ_PER_ROUND ? available : pool;
  return source.slice(0, QUIZ_PER_ROUND);
}
